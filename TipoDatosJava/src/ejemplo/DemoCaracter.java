package ejemplo;

public class DemoCaracter {
    public static void main(String[] args) {
        char letra, letra2,letra3, letra4;

        letra='a';
        letra2 = 'x';
        letra3 = '\u0023';
        letra4 = '#';
        System.out.println("La primera letra es: " + letra);
        System.out.println("La Segunda letra es: " + letra2);
        System.out.println("La Tercera letra es: " + letra3);
        System.out.println("La Cuarta letra es: " + letra4);
    }
}
