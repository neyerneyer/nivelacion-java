package ejemplo;

public class BucleFor {
    //Desarrollar un programa que muestre los números pares entre 0 – 20 de forma ascendente.
    public static void main(String[] args) {
        for (int i = 0; i <= 20; i += 2) {
            System.out.println(i + " ");
        }

        for (int i = 1; i <= 50; i++) {
            System.out.print(i + " ");
        }
    }
}
