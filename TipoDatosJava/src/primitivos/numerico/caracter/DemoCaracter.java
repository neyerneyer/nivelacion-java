package primitivos.numerico.caracter;

public class DemoCaracter {
    public static void main( String args[] ) {

        char letra1, letra2, letra3, letra4;

        letra1 = 'a';
        letra2 = 'b';
        letra3 = '\u0041';
        letra4 = '\u0023';

        System.out.println("La primera letra es : "+letra1);
        System.out.println("La segunda letra es : "+letra2);
        System.out.println("La tercera letra es : "+letra3);
        System.out.print("La tercera letra es : "+letra4);

    }
}
